public class QLTENTUOI {
    private String name;
    private int Age;

    public QLTENTUOI(String name, int Age) {
        this.name = name;
        this.Age = Age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return Age;
    }

    public void setAge(int Age) {
        this.Age = Age;
    }
    
    
    
}
